/// ══════════════════════════════════════════════════════════════════
/// MovementSorter — الفرز الزمني الموحد لحركات الكيانات
///
/// (B-1 / A-1 / A-5) الجذر التاريخي للمشكلة: أنواع الحركات المختلفة
/// تخزن `date` بصيغتين غير متجانستين:
///   - القيود الافتتاحية والفواتير:  '2026-06-10T08:30:45.123456'
///   - السندات (الشاشات القديمة):    '2026-06-10'  (يوم فقط)
///
/// المقارنة النصية المباشرة بين الصيغتين تجعل النص الأقصر
/// ('2026-06-10') "أصغر" دائماً من الكامل في نفس اليوم، فيُفرز
/// السند قبل القيد الافتتاحي مهما كان وقت إنشائه الفعلي، ولا يصل
/// الفرز أبداً إلى الترجيح بـ created_at لأن cmp != 0 دائماً —
/// فينكسر الترتيب ويُحسب الرصيد التراكمي خطأً.
///
/// الحل هنا: المقارنة على *اليوم* فقط أولاً (توحيد الصيغتين)، ثم
/// الترجيح بوقت الإنشاء الكامل `created_at` (المخزن بصيغة كاملة
/// صحيحة لكل الأنواع). متوافق مع البيانات القديمة يوم-فقط بدون أي
/// ترحيل بيانات.
///
/// هذه هي الدالة الوحيدة المعتمدة لفرز الحركات — لا تكرر منطق
/// الفرز محلياً في الشاشات (كانت توجد 5 نسخ متطابقة قبل B-1).
/// ══════════════════════════════════════════════════════════════════
class MovementSorter {
  MovementSorter._();

  /// يستخرج الجزء التاريخي (يوم) من قيمة `date` أياً كانت صيغتها.
  static String dayOf(dynamic date) =>
      (date?.toString() ?? '').split('T').first;

  /// مقارن زمني موحد: اليوم أولاً ثم `created_at` الكامل.
  ///
  /// يقبل خرائط تحتوي على المفتاحين `date` و`created_at`
  /// (كما تبنيها شاشات تفاصيل العملاء/الموردين/الموظفين/الصناديق
  /// وكشوف المصروفات). إن كانت قيمة `dateKey` فارغة/null يُستخدم
  /// `created_at` بديلاً عنها (سلوك شاشة المصروفات السابق).
  static int compare(Map<String, dynamic> a, Map<String, dynamic> b,
      {String dateKey = 'date'}) {
    final dayA = _effectiveDay(a, dateKey);
    final dayB = _effectiveDay(b, dateKey);
    final cmp = dayA.compareTo(dayB);
    if (cmp != 0) return cmp;
    return (a['created_at']?.toString() ?? '')
        .compareTo(b['created_at']?.toString() ?? '');
  }

  static String _effectiveDay(Map<String, dynamic> m, String dateKey) {
    final day = dayOf(m[dateKey]);
    if (day.isNotEmpty) return day;
    return dayOf(m['created_at']);
  }

  /// يفرز قائمة الحركات تصاعدياً (الأقدم أولاً) — الترتيب المعتمد
  /// قبل حساب الرصيد التراكمي. الفرز التنازلي للعرض يتم بعكس
  /// القائمة *بعد* حساب الرصيد، كما تفعل الشاشات حالياً.
  ///
  /// [dateKey] قابل للتخصيص للشاشات التي تستخدم مفتاحاً مختلفاً
  /// (مثل `expense_date` في كشف حساب المصروفات).
  static void sortChronologically(
    List<Map<String, dynamic>> movements, {
    String dateKey = 'date',
  }) {
    movements.sort((a, b) => compare(a, b, dateKey: dateKey));
  }
}
